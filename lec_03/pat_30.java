package lec_03;

public class pat_30 {
	public static void main(String[] args) {
		
		int n=5;
		int row=0;
		int total_star=n;
		
		
		
		while(row<n) {
			
			
			
			
			
			int cnt_star=0;
			
			int ntp=5;
			while(cnt_star<total_star) {
				
				System.out.print(ntp+"\t");
				cnt_star++;
				ntp--;
			}
			
			
			
			
			
			
			
			
			System.out.println();
			row++;
			
			
		}
	}

}
